void ImGuiManager::Render(Renderer& renderer)
{
#ifdef RABBITHOLE_USING_IMGUI
	ImGui::Render();
	ImGui_ImplVulkan_RenderDrawData(ImGui::GetDrawData(), GET_VK_HANDLE(renderer.GetCurrentCommandBuffer()));

	ImGuiIO& io = ImGui::GetIO();
	if (io.ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
	{
		ImGui::UpdatePlatformWindows();
		ImGui::RenderPlatformWindowsDefault();
	}
#endif
}

void ImGuiManager::Shutdown()
{
#ifdef RABBITHOLE_USING_IMGUI
    ImGui_ImplVulkan_Shutdown();
#endif
}

void ImGuiManager::NewFrame(float posX, float posY, float width, float height)
{
#ifdef RABBITHOLE_USING_IMGUI
	ImGui_ImplVulkan_NewFrame();
	ImGui_ImplGlfw_NewFrame();

    ImGui::SetNextWindowPos(ImVec2(posX, posY));
    ImGui::SetNextWindowSize(ImVec2(width, height));

    ImGui::NewFrame();
#endif
}
