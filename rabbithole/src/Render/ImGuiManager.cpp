void ImGuiManager::RegisterTextureForImGui(VulkanTexture* texture)
{
#ifdef RABBITHOLE_USING_IMGUI
	m_RegisteredImGuiTextures[texture] = ImGui_ImplVulkan_AddTexture(GET_VK_HANDLE_PTR(texture->GetSampler()), GET_VK_HANDLE_PTR(texture->GetView()), GetVkImageLayoutFrom(ResourceState::GenericRead));
#endif
}

VkDescriptorSet ImGuiManager::GetImGuiTextureFrom(VulkanTexture* texture)
{
#ifdef RABBITHOLE_USING_IMGUI
	auto ds = m_RegisteredImGuiTextures.find(texture);
	if (ds != m_RegisteredImGuiTextures.end())
	{
		return ds->second;
	}
	else 
	{
		ASSERT(false, "This texture is not registered for ImGui usage");
		return VK_NULL_HANDLE;
	}
#else
	return VK_NULL_HANDLE;
#endif
}

